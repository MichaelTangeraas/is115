<p>In module 7.3, we are demonstrating how we can create backup copies of our databases.</p>

<hr />

<h2>phpMyAdmin - Export and Import DB</h2>

<ul>
    <h3>Export db content</h3>
    <li>Open phpMyAdmin</li>
    <li>Select the database you want to export</li>
    <li>Click on the Export tab</li>
    <li>Click on the Go button</li>
    <li>Save the file to your computer</li>

    <h3>Delete DB</h3>
    <li>Open phpMyAdmin</li>
    <li>Select the database you want to delete</li>
    <li>Click on the Operations tab</li>
    <li>Click on the Drop the database (DROP) button</li>
    <li>Click on the Yes button</li>
    <br />
    <li>Alternatively, you can write the SQL command to delete the database - DROP DATABASE `db_name`</li>


    <h3>Import db content</h3>
    <li>Open phpMyAdmin</li>
    <li>Create a new database</li>
    <li>Select the database you want to import to</li>
    <li>Click on the Import tab</li>
    <li>Click on the Choose File button</li>
    <li>Select the file you want to import</li>
    <li>Click on the Go button</li>
</ul>


